package exTriangulo;

public class Figura2D {
    //atributes
    private double base;
    private double altura;
    
    public Figura2D(double base, double altura) {
        this.base = base;
        this.altura = altura;
    }

    //Getters and Setters
    public double getBase() {
        return base;
    }

    public void setBase(double base) {
        this.base = base;
    }

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }
    
}
